        public override FontCharacter GetFontData(char character)
        {
            if (character < 32 || character > 127)
            {
                character = '?';
            }

            var result = new FontCharacter();

            result.Data = font8x8_basic[character - 32];
            result.Width = _characterWidth;
            result.Height = _characterHeight;
            result.Space = 1;

            return result;
        }

        public override byte SpaceWidth
        {
            get { return 8; }
        }
